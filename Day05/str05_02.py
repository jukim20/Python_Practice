# 문자열의 연산
# 1) 문자열끼리 연산 +
str1 = "안녕"
str2 = "하세요"
print(str1 + str2)
print(str1)
str3 = str1 + str2
print(str3)
num1 = 10
num1 += 10  # num1 = num1 + 10 의 축약형 ( += )
print(num1)
msg = "python    "
msg += "java    "
msg += "cpp     "
msg += "c"
print(msg)
#  2) 다른 타입과의 연산 ( , ) 쉼표로 구분
print("안녕", 10)
# 3 ) *
print("hello " * 5)  # hello hello hello hello hello


