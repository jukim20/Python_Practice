# 조건문 (if)

"""
1. if      ==> 키워드
2. True    ==> 조건이 참이면
3. :       ==> 조건의 끝
4. 들여쓰기 ==> 조건이 참이면 실행문이 실행된다.
               (들여쓰기를 해야한다, space 4번)
               if 문의 영역범위를 의미한다.

"""

if True:
    print("조건문 실행")  # 들여쓰기를 해야한다 (space 4번)
    print("조건문")  # 몇 줄을 쓰던 상관 없음

print("다 실행")  # 조건과 상관없이 실행됨

if False:
    print("조건문 실행2")  # 아무 것도 프린트 되지 않음

print("다 실행2")

